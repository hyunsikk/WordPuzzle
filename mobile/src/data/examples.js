// Get all SAT words for progress tracking
export const getSATWords = () => {
  return Object.keys(wordExamples).filter(word => wordExamples[word].isSATWord);
};

// Get essential SAT words (top 500 most tested)
export const getEssentialSATWords = () => {
  // This is a curated list of the most frequently tested SAT vocabulary
  return getSATWords().slice(0, Math.min(500, getSATWords().length));
};

// Get random example sentence for a word
export const getRandomExample = (word) => {
  const wordData = wordExamples[word.toUpperCase()];
  if (!wordData || !wordData.examples || wordData.examples.length === 0) {
    return null;
  }
  const randomIndex = Math.floor(Math.random() * wordData.examples.length);
  return wordData.examples[randomIndex];
};

// Get word data for quiz use
export const getWordData = (word) => {
  return wordExamples[word.toUpperCase()] || null;
};

// Get fill-in-the-blank sentence (replaces word with blank)
export const getFillInBlank = (word) => {
  const example = getRandomExample(word);
  if (!example) return null;
  
  const upperWord = word.toUpperCase();
  const variations = [
    upperWord,
    word.toLowerCase(),
    word.charAt(0).toUpperCase() + word.slice(1).toLowerCase()
  ];
  
  for (const variation of variations) {
    if (example.includes(variation)) {
      return example.replace(variation, "______");
    }
  }
  
  return null;
};

